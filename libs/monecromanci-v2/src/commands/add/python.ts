/**
 * The `nx-release-publish` target for a publishable Python library.
 *
 * @remarks
 * `--skip-existing` mirrors npm's own idempotent-publish behaviour (Nx's
 * default `@nx/js:release-publish` also tolerates a version already on the
 * registry), so a re-run after a partial release failure does not hard-error
 * on the packages that already made it. Credentials
 * (`TWINE_USERNAME`/`TWINE_PASSWORD`/`TWINE_REPOSITORY_URL`) are read from the
 * environment by twine itself — set by CI (see `overlay.ts`'s
 * `azurePipelinesYaml`), never written to disk here.
 *
 * `nx release publish --dry-run` (the workspace's `release:preview` script)
 * appends a literal `--dryRun=true` to whatever command a plain
 * `nx:run-commands` `nx-release-publish` target runs — unlike
 * `@nx/js:release-publish`, a real executor that parses and honours the flag
 * `nx:run-commands` has no such awareness (verified empirically: the
 * unguarded command hard-fails, twine rejecting the unrecognized argument).
 * The `node -e` wrapper below detects that flag and prints a preview instead
 * of shelling out to twine for real.
 *
 * @param root - Workspace-relative project directory.
 * @returns The nx:run-commands target object.
 * @throws Never - pure object construction.
 * @typeParam None - this function has no generic type parameters.
 */
function pythonReleasePublishTarget (root: string): Record<string, unknown> {
  // The trailing `--` matters: nx appends --dryRun=true straight onto this
  // command string, and without `--` node itself tries (and fails) to parse
  // it as a node CLI flag rather than handing it to the script as argv.
  const command = `node -e "const isDryRun=process.argv.slice(1).some((a)=>a.startsWith('--dryRun'));if(isDryRun){console.log('[dry-run] would run: python3 -m twine upload --skip-existing dist/*');process.exit(0)}process.exit(require('node:child_process').spawnSync('python3',['-m','twine','upload','--skip-existing','dist/*'],{stdio:'inherit'}).status??1)" --`
  return {
    executor:  'nx:run-commands',
    dependsOn: ['build'],
    options:   { command, cwd: root },
  }
}

/**
 * The `package` target for a Python app: zip its built wheel into the drop.
 *
 * @remarks
 * Zips `apps/<name>/dist` (the {@link pythonBuildTarget} wheel + sdist) into
 * `dist/drop/python-app-<name>.zip` — basename exactly `python-app-<name>`,
 * the string CI turns into the per-app build tag. `dependsOn: build`
 * guarantees the wheel exists; same cross-platform `adm-zip` one-liner used
 * throughout `add`.
 *
 * @param name - The Python app's project name.
 * @returns The nx:run-commands target object.
 * @throws Never - pure object construction.
 * @typeParam None - this function has no generic type parameters.
 */
function pythonAppPackageTarget (name: string): Record<string, unknown> {
  const zip = `dist/drop/python-app-${name}.zip`
  const command = `node -e "const fs=require('node:fs');fs.mkdirSync('dist/drop',{recursive:true});const A=require('adm-zip');const z=new A();z.addLocalFolder('apps/${name}/dist');z.writeZip('${zip}')"`
  return {
    executor:  'nx:run-commands',
    dependsOn: ['build'],
    // eslint-disable-next-line unicorn/no-incorrect-template-string-interpolation -- {workspaceRoot} is an Nx output token
    outputs:   [`{workspaceRoot}/${zip}`],
    options:   { command },
  }
}

/**
 * The `package` target for a Python Azure Function: zip the deploy folder.
 *
 * @remarks
 * An Azure Functions Python app is deployed as **source** (the host installs
 * `requirements.txt` and runs `function_app.py`), not as a wheel — so this
 * zips `function_app.py` + `host.json` + `requirements.txt` + the module
 * package into `dist/drop/python-function-app-<name>.zip`. Basename exactly
 * `python-function-app-<name>` (the CI build tag). No `build` dependency (the
 * artifact is source), same `adm-zip` one-liner.
 *
 * @param name - The function app's project name.
 * @param moduleDirectory - The app's Python module directory basename.
 * @returns The nx:run-commands target object.
 * @throws Never - pure object construction.
 * @typeParam None - this function has no generic type parameters.
 */
function pythonFunctionAppPackageTarget (name: string, moduleDirectory: string): Record<string, unknown> {
  const zip = `dist/drop/python-function-app-${name}.zip`
  const root = `apps/${name}`
  const command = `node -e "const fs=require('node:fs');fs.mkdirSync('dist/drop',{recursive:true});const A=require('adm-zip');const z=new A();z.addLocalFile('${root}/function_app.py');z.addLocalFile('${root}/host.json');z.addLocalFile('${root}/requirements.txt');z.addLocalFolder('${root}/${moduleDirectory}','${moduleDirectory}');z.writeZip('${zip}')"`
  return {
    executor: 'nx:run-commands',
    // eslint-disable-next-line unicorn/no-incorrect-template-string-interpolation -- {workspaceRoot} is an Nx output token
    outputs:  [`{workspaceRoot}/${zip}`],
    options:  { command },
  }
}

