/**
 * The vendoring-aware build helper written once to `tools/python-build.js`.
 *
 * @remarks
 * Plain pip has no `bundleLocalDependencies` equivalent, so a project that
 * imports a workspace-internal Python library needs its own vendoring step: a
 * hand-added `[tool.mnci2] vendor = ["<lib>"]` table in the consuming
 * project's `pyproject.toml` (the pip-world counterpart of hand-wiring a
 * `dependencies = [...]` entry — mnci2 performs no automatic cross-project
 * dependency wiring for any kind) tells this script which sibling internal
 * libraries (always at `libs/<name>`, mnci2's fixed internal-lib location) to
 * copy into a staged copy of the project before running `python -m build` —
 * so the resulting wheel contains the vendored module as a real top-level
 * package, exactly as if it had been written there directly. Projects with no
 * `vendor` entry build straight from their own directory, no staging.
 */
export const PYTHON_BUILD_SCRIPT = `#!/usr/bin/env node
// Generated by MoNecromanCI v2. Builds a Python project's wheel/sdist via
// \`python -m build\`. When the project's pyproject.toml declares
// [tool.mnci2] vendor = ["some-internal-lib"], the named libs/<name> module is
// copied into a staged copy of the project first, so the built wheel bundles
// it as a real top-level package (plain pip has no bundled-local-dependency
// feature of its own).
'use strict'

const { execFileSync } = require('node:child_process')
const { cpSync, mkdtempSync, readFileSync, rmSync, writeFileSync } = require('node:fs')
const { tmpdir } = require('node:os')
const { join } = require('node:path')

const projectRoot = process.argv[2]
if (!projectRoot) {
  console.error('Usage: node tools/python-build.js <project-root>')
  process.exit(1)
}

const outDir = join(process.cwd(), projectRoot, 'dist')
const pyproject = readFileSync(join(projectRoot, 'pyproject.toml'), 'utf-8')
const vendorMatch = /^\\s*vendor\\s*=\\s*\\[([^\\]]*)\\]/m.exec(pyproject)
const vendorNames = vendorMatch
  ? vendorMatch[1].split(',').map((entry) => entry.trim().replace(/^["']|["']$/g, '')).filter(Boolean)
  : []

if (vendorNames.length === 0) {
  execFileSync('python3', ['-m', 'build', '--outdir', outDir, projectRoot], { stdio: 'inherit' })
  process.exit(0)
}

const stagingRoot = mkdtempSync(join(tmpdir(), 'mnci2-python-build-'))
cpSync(projectRoot, stagingRoot, { recursive: true })

const moduleDirs = vendorNames.map((name) => name.replaceAll('-', '_'))
for (const [index, name] of vendorNames.entries()) {
  cpSync(join('libs', name, moduleDirs[index]), join(stagingRoot, moduleDirs[index]), { recursive: true })
}

const stagedPyprojectPath = join(stagingRoot, 'pyproject.toml')
const stagedPyproject = readFileSync(stagedPyprojectPath, 'utf-8')
const patched = stagedPyproject.replace(/^(\\s*packages\\s*=\\s*)\\[([^\\]]*)\\]/m, (_match, prefix, existing) => {
  const names = existing.split(',').map((entry) => entry.trim().replace(/^["']|["']$/g, '')).filter(Boolean)
  const merged = [...new Set([...names, ...moduleDirs])]
  return \`\${prefix}[\${merged.map((name) => \`"\${name}"\`).join(', ')}]\`
})
writeFileSync(stagedPyprojectPath, patched)

try {
  execFileSync('python3', ['-m', 'build', '--outdir', outDir, stagingRoot], { stdio: 'inherit' })
} finally {
  rmSync(stagingRoot, { recursive: true, force: true })
}
`

/**
 * The hand-written `VersionActions` implementation written once to
 * `tools/python-version-actions.js`.
 *
 * @remarks
 * Registered per-project (see {@link addPythonLib}, the only Python kind that
 * gets a `release.version.versionActions` override) so `nx release` versions
 * publishable Python libs the same way it versions npm packages — from
 * conventional commits — without `@nxlv/python`. Reads and writes the
 * `version = "..."` line under `pyproject.toml`'s `[project]`
 * table with a regex, safe because mnci2 authors that file's exact shape
 * itself (same reasoning the plain-JSON `readJson`/`toJson` helpers rely on
 * elsewhere, applied to the one line of TOML this needs — no TOML dependency
 * pulled in for it). Internal-lib dependencies are vendored (bundled into the
 * wheel), not registry references, so dependency version resolution/update
 * are no-ops — the same branch `@nxlv/python`'s own reference implementation
 * takes for bundled dependencies.
 */
export const PYTHON_VERSION_ACTIONS_SCRIPT = `// Generated by MoNecromanCI v2. Hand-written Nx release VersionActions for
// pip-native Python packages (no uv, no Poetry) — reads/writes the
// \`version = "..."\` line under pyproject.toml's [project] table.
'use strict'

const { readFileSync } = require('node:fs')
const { execFileSync } = require('node:child_process')
const { join } = require('node:path')
const { VersionActions } = require('nx/src/command-line/release/version/version-actions')

const VERSION_LINE = /^version\\s*=\\s*"([^"]+)"/m

class PythonVersionActions extends VersionActions {
  validManifestFilenames = ['pyproject.toml']

  async readCurrentVersionFromSourceManifest (tree) {
    const manifestPath = join(this.projectGraphNode.data.root, 'pyproject.toml')
    const content = tree.read(manifestPath, 'utf-8')
    if (content === null) {
      return null
    }
    const match = VERSION_LINE.exec(content)
    if (!match) {
      throw new Error(\`Could not find a "version = ..." line under [project] in \${manifestPath}\`)
    }
    return { currentVersion: match[1], manifestPath }
  }

  async readCurrentVersionFromRegistry (_tree, _currentVersionResolverMetadata) {
    const name = this.projectGraphNode.name
    try {
      const output = execFileSync('python3', ['-m', 'pip', 'index', 'versions', name], { encoding: 'utf-8', stdio: ['ignore', 'pipe', 'ignore'] })
      const match = /Available versions:\\s*([^\\s,]+)/.exec(output)
      return { currentVersion: match ? match[1] : null, logText: 'from pip index versions' }
    } catch {
      return { currentVersion: null, logText: 'package not found on registry' }
    }
  }

  async readCurrentVersionOfDependency (_tree, _projectGraph, _dependencyProjectName) {
    // Internal-lib dependencies are vendored (copied into the wheel at build
    // time), not registry-referenced, so there is no separate version to track.
    return { currentVersion: null, dependencyCollection: null }
  }

  async updateProjectVersion (tree, newVersion) {
    const manifestPath = join(this.projectGraphNode.data.root, 'pyproject.toml')
    const content = tree.read(manifestPath, 'utf-8')
    const updated = content.replace(VERSION_LINE, \`version = "\${newVersion}"\`)
    tree.write(manifestPath, updated)
    return [\`Updated \${manifestPath} to version \${newVersion}\`]
  }

  async updateProjectDependencies (_tree, _projectGraph, _dependenciesToUpdate) {
    // Same reasoning as readCurrentVersionOfDependency: vendored, not registry deps.
    return []
  }
}

module.exports = PythonVersionActions
`

/**
 * Idempotently writes the shared pip-native Python tooling into the workspace.
 *
 * @remarks
 * Lazy, like {@link ensureAdmZip}: written on the first Python `add` of any
 * kind, not unconditionally by `mnci2 new` — a pure-JS/TS workspace never
 * gains these files. `tools/python-build.js` and `tools/python-version-actions.js`
 * are written verbatim (no per-project templating); `requirements-dev.txt`
 * only when absent, so a user's own edits to it (extra dev tools) survive
 * repeat `add` calls.
 *
 * @param workspaceRoot - Absolute path to the workspace.
 * @returns Nothing.
 * @throws Propagates any Node.js `fs` error raised while writing.
 * @typeParam None - this function has no generic type parameters.
 */
function ensurePythonTooling (workspaceRoot: string): void {
  writeFileEnsured(join(workspaceRoot, 'tools', 'python-build.js'), PYTHON_BUILD_SCRIPT)
  writeFileEnsured(join(workspaceRoot, 'tools', 'python-version-actions.js'), PYTHON_VERSION_ACTIONS_SCRIPT)
  const requirementsDevPath = join(workspaceRoot, 'requirements-dev.txt')
  if (!fileExists(requirementsDevPath)) {
    writeFileEnsured(requirementsDevPath, PYTHON_REQUIREMENTS_DEV)
  }
}

/**
 * A Python project's module directory name, derived from its Nx project name.
 *
 * @remarks
 * Python identifiers cannot contain hyphens, so `my-svc` becomes `my_svc` —
 * the same convention `@nxlv/python` used, kept for continuity.
 *
 * @param name - The Nx project name (already validated).
 * @returns The module directory's basename.
 * @throws Never - pure string mapping.
 * @typeParam None - this function has no generic type parameters.
 */
function pythonModuleDirectory (name: string): string {
  return name.replaceAll('-', '_')
}

/**
 * The `pyproject.toml` written for a buildable Python project (app or lib).
 *
 * @remarks
 * `hatchling` is the PEP 517 backend (unchanged from the `@nxlv/python`-era
 * choice); `[tool.hatch.build.targets.wheel] packages` is explicit (not
 * hatchling's own auto-detection) because {@link PYTHON_BUILD_SCRIPT} patches
 * this exact list when vendoring an internal lib's module into the wheel.
 * `[project.dependencies]` starts empty — a project that needs a real
 * external PyPI dependency (or, per {@link PYTHON_BUILD_SCRIPT}'s remarks, a
 * vendored internal one via `[tool.mnci2] vendor = [...]`) is wired by hand
 * after generation, same as every other cross-project dependency mnci2 leaves
 * to the user.
 *
 * @param name - The project name (already validated).
 * @param moduleDirectory - The project's module directory basename.
 * @returns The `pyproject.toml` contents.
 * @throws Never - pure string build.
 * @typeParam None - this function has no generic type parameters.
 */
function pythonPyprojectToml (name: string, moduleDirectory: string): string {
  return `[project]
name = "${name}"
version = "1.0.0"
description = ""
requires-python = ">=3.9"
dependencies = []

[build-system]
requires = ["hatchling"]
build-backend = "hatchling.build"

[tool.hatch.build.targets.wheel]
packages = ["${moduleDirectory}"]

[tool.pytest.ini_options]
testpaths = ["tests"]
`
}

