/**
 * Adds a publishable Python library under `python-packages/`.
 *
 * @remarks
 * `nx-release-publish` (twine) is a hand-authored target, not a plugin hook —
 * so the shared `nx release` (scoped to `python-packages/*` — see
 * `overlay.ts`'s `RELEASE_CONFIG`) versions, tags and publishes it via
 * {@link pythonReleasePublishTarget}. `release.version.versionActions` is set
 * here, on the project itself (not the workspace-level release config), so
 * `nx release` reads/writes this project's `pyproject.toml` via the
 * hand-written `PythonVersionActions` instead of npm's default.
 *
 * @param workspaceRoot - Absolute path to the workspace.
 * @param name - The project name (already validated).
 * @returns Nothing.
 * @throws Error when Python is missing.
 * @typeParam None - this function has no generic type parameters.
 */
export function addPythonLib (workspaceRoot: string, name: string): void {
  ensurePython(workspaceRoot)
  ensurePythonTooling(workspaceRoot)

  const root = `python-packages/${name}`
  const absoluteRoot = join(workspaceRoot, root)
  const moduleDirectory = pythonModuleDirectory(name)
  writeFileEnsured(join(absoluteRoot, 'pyproject.toml'), pythonPyprojectToml(name, moduleDirectory))
  writeFileEnsured(join(absoluteRoot, moduleDirectory, '__init__.py'), pythonSampleModule(moduleDirectory))
  writeFileEnsured(join(absoluteRoot, 'tests', `test_${moduleDirectory}.py`), pythonSampleTest(moduleDirectory))
  writePythonProjectJson(absoluteRoot, name, 'library', {
    lint:                 pythonLintTarget(root),
    test:                 pythonTestTarget(root),
    build:                pythonBuildTarget(root),
    'nx-release-publish': pythonReleasePublishTarget(root),
  }, { version: { versionActions: 'tools/python-version-actions.js' } })
}

